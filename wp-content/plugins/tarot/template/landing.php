            <details class="trt-faq-item">
                <summary class="trt-faq-q">Can Tarot accurately predict the future?</summary>
                <div class="trt-faq-a">Tarot does not offer a fixed or single version of the future. The cards reflect current energies and tendencies, and the future shifts as actions and awareness shift. Tarot's role is to illuminate what is already in motion, not to deliver a verdict on what will happen.</div>
            </details>
