                <summary class="trt-faq-q">How often should I do a reading?</summary>
                <div class="trt-faq-a">There's no strict rule. Many people pull one card each morning for daily focus, or do a full reading when facing an important decision. That said, repeating the same question multiple times in one day tends to produce little value, since the cards reflect a current state and that state needs time to change.</div>
            </details>
